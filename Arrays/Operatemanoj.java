public class Operatemanoj{
  public static void main(String args[]){
    int x=1000;
    int y=40;
 
    System.out.println("x+y= "+(x+y));
    System.out.println("x-y= "+(x-y));
    System.out.println("x*y= "+(x*y));
    System.out.println("x%y= "+(x%y));
    System.out.println("x/y= "+(x/y));
    y++;
    System.out.println("y++= "+(y++));
    System.out.println("y= "+(y));
    System.out.println("++x= "+(++x));
  } 
} 